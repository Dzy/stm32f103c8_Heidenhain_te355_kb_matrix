/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */
  static uint32_t KBSTATE = 11;
  static uint8_t KBSCAN = 0;

  if(KBENABLE) {

      switch(KBSTATE) {

        case 11:
            HAL_GPIO_WritePin(GPIOA, GPIO_PIN_2|GPIO_PIN_3|GPIO_PIN_4|GPIO_PIN_5|GPIO_PIN_6|GPIO_PIN_7|GPIO_PIN_8|GPIO_PIN_9, GPIO_PIN_SET);
            HAL_GPIO_WritePin(GPIOA, (1<<SL)<<2, GPIO_PIN_RESET);
            HAL_GPIO_WritePin(GPIOA, (1<<SL)<<2, GPIO_PIN_RESET);
            HAL_GPIO_WritePin(GPIOA, (1<<SL)<<2, GPIO_PIN_RESET);
            HAL_GPIO_WritePin(GPIOA, (1<<SL)<<2, GPIO_PIN_RESET);
            KBSTATE++;
        break;

        case 12:
            KBSCAN = ((uint8_t)~(((GPIOB->IDR & 0x1f8)>>1)|(GPIOB->IDR & 0x03)));
            if(KBSCAN!=0) {
                HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);
                snprintf(Buf,128, "D%03d", keymap[(((31-__CLZ(KBSCAN))) + (SL*8))]);
                CDC_Transmit_FS(Buf, 4);
                KBSTATE++;
            }else{
                SL++;
                SL &= 0x7;
                KBSTATE = 11;
            }
        break;

        case 13:
            KBSCAN = ((uint8_t)~(((GPIOB->IDR & 0x1f8)>>1)|(GPIOB->IDR & 0x03)));
            if(KBSCAN==0) {
                HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);
                snprintf(Buf,128, "U\n");
                CDC_Transmit_FS(Buf, 2);
                SL++;
                SL &= 0x7;
                KBSTATE = 11;
            }
        break;

        }
    }
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */

  /* USER CODE END SysTick_IRQn 1 */
}

